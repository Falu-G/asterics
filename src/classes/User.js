class User{
    constructor(firstname,username,lastname,address, email,password,confirmPassword,phone,city,state,country){

       this.firstname = firstname;
       this.username = username;
       this.lastname = lastname;
       this.address = address;
       this.email = email;
       this.password = password;
       this.confirmPassword = confirmPassword;
       this.phone = phone;
       this.city = city;
       this.state = state;
       this.country = country;
    }
}

export default User;
